public class 反转链表 {

    public ListNode reverseList(ListNode head) {
        if (head == null) return head;
        ListNode cur = head;
        ListNode pre = null;
        ListNode next = null;

        while (cur != null) {
            next = cur.next;
            cur.next = pre;
            pre = cur;
            cur = next;
        }
        return pre;
    }
}
